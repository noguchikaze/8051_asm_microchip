;RE
;MI
;FA
;SOL
;LA
;SI
;HDO
;===========================================
;SHORT DELAY FOR KEYBOARD
KDELAY:
	MOV	R5,#1FH
K:
	MOV	R4,#1FH
	DJNZ	R4,$
	DJNZ	R5,K
	RET
BDELAY:
	MOV	R5,#3FH
K2:
	MOV	R4,#1FH
	DJNZ	R4,$
	DJNZ	R5,K2
	RET

;LONG DELAY FOR COUNTDOWN
LDELAY:
  MOV R3,#015H ;12
;  MOV R3,#0CH ;12
L1:
  MOV R5,#0FFH  ;213
;  MOV R5,#0D5H  ;213
L:
  MOV R4,#0FFH  ;194
;  MOV R4,#0C2H  ;194
  DJNZ	R4,$
  DJNZ	R5,L
  DJNZ	R3,L1
  RET
;==========================================
TOEND:
END
