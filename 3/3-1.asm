ORG	0000H
AJMP	MAIN
ORG	0050H

MAIN:
	MOV	DPTR,#SSTABLE
LOOP:
	MOV 	R2,#0H	;R2=counter
LOOP1:

	MOV	A,R2
	MOVC	A,@A+DPTR
	MOV	P0,A
	INC	R2	
	ACALL	DELAY
	CJNE	R2,#10,LOOP1	;if R2!=10, jump to Loop 1
	AJMP	LOOP

SSTABLE:
	DB	0C0H	;0
	DB	0F9H	;1
	DB	0A4H	;2
	DB	0B0H	;3
	DB	099H	;4
	DB	092H	;5
	DB	082H	;6
	DB	0F8H	;7
	DB	080H	;8
	DB	090H	;9


DELAY:
	MOV	R5 ,#0FFH
DELAY1:
	MOV	R6 ,#0FFH
DELAY2:
	MOV	R7 ,#10H
DELAY3:
	DJNZ	R7,DELAY3
	DJNZ	R6,DELAY2
	DJNZ	R5,DELAY1
	RET
END