ORG	0000H
AJMP	MAIN
ORG	0050H

MAIN:
	MOV	DPTR,#SSTABLE
LOOP:
	MOV 	R2,#0H	;R2=counter
LOOP1:

	MOV	A,R2
	MOVC	A,@A+DPTR
	MOV	P0,A
	INC	R2	
	ACALL	DELAY
	CJNE	R2,#6,LOOP1	;if R2!=10, jump to Loop 1
	AJMP	LOOP

SSTABLE:
	DB	10001000B	;A
	DB	10000011B	;b
	DB	11000110B	;C
	DB	10100001B	;d
	DB	10000110B	;E
	DB	10001110B	;F


DELAY:
	MOV	R5 ,#0FFH
DELAY1:
	MOV	R6 ,#0FFH
DELAY2:
	MOV	R7 ,#10H
DELAY3:
	DJNZ	R7,DELAY3
	DJNZ	R6,DELAY2
	DJNZ	R5,DELAY1
	RET
END