ORG	00H
AJMP	MAIN
ORG	50H

MAIN:

	MOV A,#80H
	MOV	R3,A
	MOV	R1,#0FH
LOOP:

 	MOV	A,R3
	MOV	R1,P1  ;P0=I/O

	RL	A	   ;SPIN A

	MOV	R3,A	;LET R3 BE THE COPY OF A

	MOV	P0,A	   ;P1=LED

	MOV	A,R1	   ;MOV I/O TO A
	ADD	A,#01H
	ACALL	DELAY
	AJMP	LOOP


DELAY:

	MOV R5,	#0FFH
DELAY1:
	MOV R6,#0FFH
DELAY2:
	MOV R7,	A
DELAY3:
	DJNZ	R7,DELAY3
	DJNZ	R6,DELAY2
	DJNZ	R5,DELAY1

	RET
	END